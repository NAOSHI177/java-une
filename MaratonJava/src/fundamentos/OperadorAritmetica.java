package fundamentos;


public class OperadorAritmetica {
	public static void main(String[] args){
	
		int primerValor=35;
		int segundoValor=20;
		
		System.out.println("La concatenacion es "+ primerValor + segundoValor);	
		System.out.println("La suma es "+ (primerValor + segundoValor));	
		System.out.println("La resta es "+ (primerValor - segundoValor));	
		System.out.println("La multiplicación es "+ (primerValor * segundoValor));	
		System.out.println("La división es "+ (primerValor / segundoValor));
		System.out.println("El resto es "+ (primerValor % segundoValor));			

	}
}